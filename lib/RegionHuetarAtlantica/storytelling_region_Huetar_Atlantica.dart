  /// Create one series with sample hard coded data.
  static List<charts.Series<LinearSales, int>> _createSampleData() {
    final data = [
      new LinearSales(2010, 12.74),
      new LinearSales(2011, 12.62),
      new LinearSales(2012, 12.5),
      new LinearSales(2013, 12.87),
      new LinearSales(2014, 12.37),
      new LinearSales(2015, 12.25),
      new LinearSales(2016, 12.37),
      new LinearSales(2017, 12.25),
    ];

    return [
      new charts.Series<LinearSales, int>(
        id: 'Sales',
        domainFn: (LinearSales sales, _) => sales.year,
        measureFn: (LinearSales sales, _) => sales.sales,
        colorFn: (_, __) => charts.MaterialPalette.deepOrange.shadeDefault,
        data: data,
        // Set a label accessor to control the text of the arc label.
        labelAccessorFn: (LinearSales row, _) => '${row.year}: ${row.sales}',
      )
    ];
  }

